package CompSCi;

public class SimpleCalculator {
    public static void main(String[] args) {
        int num1;
        int num2;
        num1 = 24;
        num2 = 33333;
        System.out.println("The sum of both numbers is: " + (num1 + num2));
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1/num2);
        System.out.println(num1 % num2);
    }
}
