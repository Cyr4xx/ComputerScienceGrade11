package CompSCi;

public class AreaPerimeterRec {
    public static void main(String[] args){
        int length;
        int width;
        length = 25;
        width = 15;

        System.out.println("The perimeter of the rectangle: " + (2 * length + 2 * width));
        System.out.println("The area of the rectangle: " + (length * width));

    }
}
